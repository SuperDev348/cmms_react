export default function* rootSaga() {
  yield all([
    yield takeEvery(actions.ADD_BUSINESS_USER,   addBusinessUser),
    yield takeEvery(actions.GET_BUSINESS_USERS, getBusinessUser),
    yield takeEvery(actions.GET_ASSET_USERS_BY_GROUP_ID, getAssetUsersByGroupId),
    yield takeEvery(actions.UPDATE_BUSINESS_USER, updateBusinessUser),
    yield takeEvery(actions.DELETE_BUSINESS_USER, deleteBusinessUser), 
    
  ]);
}
